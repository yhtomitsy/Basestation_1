/* Function to verify that the image sensor is the OV5640 chip 
 * Sets up flag glIsValidSensor which is to be verified before 
 * any other public calls are executed*/
CyU3PReturnStatus_t OV5640_VerifyChipId()
{

    uint8_t readBuffer [2];

    CyU3PReturnStatus_t status = CY_U3P_SUCCESS;

    readBuffer [0] = readBuffer [1] = 0;
    status = OV5640_SensorRead(OV5640_CHIP_ID_HIGH_BYTE, 2, readBuffer);
    if (status != CY_U3P_SUCCESS)
        return status;

    if (OV5640_CHIP_ID != CY_U3P_MAKEWORD(readBuffer[0], readBuffer[1]))
    {
        status = CY_U3P_ERROR_BAD_ARGUMENT;
    }
    else
    {
        glIsValidSensor = CyTrue;
    }

#ifdef OV5640_DEBUG
    CyU3PDebugPrint(4,"\r\nChip ID Read= 0x%x",CY_U3P_MAKEWORD(readBuffer[0], readBuffer[1]));
#endif
    return status;
}


CyU3PReturnStatus_t OV5640_WriteConfigurationSettings (CyOv5640Reg_t * configSettings, uint16_t configSettingsSize)
{
    uint16_t regCounter = 0;
    CyU3PReturnStatus_t status = CY_U3P_SUCCESS;
    uint8_t rwBuffer;
#ifdef OV5640_DEBUG
    CyU3PDebugPrint (4,"\r\nWriting Configuration Settings:");
#endif

    for (regCounter = 0; ((regCounter < configSettingsSize) && (status == CY_U3P_SUCCESS)); regCounter++)
    {
#ifdef OV5640_DEBUG
        CyU3PDebugPrint (4,"\r\nRegister = 0x%x: Value = 0x%x",configSettings[regCounter].regAddr, 
                configSettings[regCounter].regValue);
#endif
        rwBuffer = configSettings[regCounter].regValue;
        status = OV5640_SensorWrite (configSettings[regCounter].regAddr, 1, &rwBuffer); 
    }

    return status;
}

