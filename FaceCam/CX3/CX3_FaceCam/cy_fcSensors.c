CyU3PReturnStatus_t CyCx3_ImageSensor_Sleep()
{
    CyU3PReturnStatus_t status = CY_U3P_SUCCESS;
    uint8_t rwBuffer = 0;

    if (glIsValidSensor != CyTrue)
        return CY_U3P_ERROR_NOT_SUPPORTED;

    status = OV5640_SensorRead(OV5640_SYSTEM_CTRL0, 1, &rwBuffer);
    if (status == CY_U3P_SUCCESS)
    {
        rwBuffer |= OV5640_SYSTEM_POWER_DOWN_MASK;
        status = OV5640_SensorWrite(OV5640_SYSTEM_CTRL0, 1, &rwBuffer);
    }
    return status;
}

CyU3PReturnStatus_t CyCx3_ImageSensor_Wakeup()
{
    CyU3PReturnStatus_t status = CY_U3P_SUCCESS;
    uint8_t rwBuffer = 0;

    rwBuffer = 0x21;

    if (glIsValidSensor != CyTrue)
        return CY_U3P_ERROR_NOT_SUPPORTED;

    status = OV5640_SensorWrite(OV5640_POLARITY_CTRL00, 1, &rwBuffer);
    if (status == CY_U3P_SUCCESS)
    {
        rwBuffer = 0;
        status = OV5640_SensorRead(OV5640_SYSTEM_CTRL0, 1, &rwBuffer);
        if (status == CY_U3P_SUCCESS)
        {
            rwBuffer &= (~OV5640_SYSTEM_POWER_DOWN_MASK);
            status = OV5640_SensorWrite(OV5640_SYSTEM_CTRL0, 1, &rwBuffer);
        }
    }
    return status;
}

